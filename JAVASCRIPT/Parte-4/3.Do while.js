/*
While e do while

while -> Verifique se e true e faça
do while -> Faça, depois veja se e true
*/
let i = 0
console.log("Comeca um codigo aqui")

do {  
    
    i++
    console.log(i)
    
   
} while (i < 30);
console.log("Termina um codigo aqui")

