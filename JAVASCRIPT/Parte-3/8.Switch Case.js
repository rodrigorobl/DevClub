/*

    Switch Case - Controlador de Fluxo
*/
const temperatury = 20

switch (temperatury) {
        case 10:
            console.log("Esta frio")
        break
        
        case 20:
            console.log("Esta meio frio")
        break

        case 30:
            console.log("Esta quente")

        break
        
        default:
            console.log("Nao sei dizer")
        break

}